void fpga_hsm2_init(void)
{
	//sm2 reset
	FPGA_REG(FPGA_RESET_SM2_ADDR)=0x00;
	delay_ms(5);
	print(PRINT_FPGA,"F_RE_SM2 %x\r\n",FPGA_REG(FPGA_RESET_SM2_ADDR));
	FPGA_REG(FPGA_RESET_SM2_ADDR)=0x01;
	print(PRINT_FPGA,"F_RE_SM2 %x\r\n",FPGA_REG(FPGA_RESET_SM2_ADDR));
	delay_ms(5);
	FPGAHeader fpga_header;
	uint8_t *alg_hdr;
	uint8_t *data_ptr;

	memset(&fpga_header, 0, sizeof(FPGAHeader));
	//step: init 0
	while(fpga_write_start()==REG_REST) {
		fpga_reset_mcu();
		delay_ms(5);
	}
	fpga_header.mark = FPGA_HEAD_MARK;
	fpga_header.src = FPGA_DATA_ARM;
	fpga_header.dst = FPGA_DATA_SM2_HSM2;
	fpga_header.channel = FPGA_CHANNEL_DEF;
	fpga_header.sm2_cmd = CMD_SM2_INIT0;
	fpga_header.pkglen = FPGA_DATAHEAD_LEN + FPGA_ALGHEAD_LEN + FPGA_DATA_LEN(sizeof(sm2_init0_data));
	alg_hdr = set_fpga_header((uint8_t *)FPGA_DATA_WRITE_ADDR, &fpga_header);
	data_ptr = alg_header(alg_hdr, 0, FPGA_ALGHEAD_LEN + FPGA_DATA_LEN(sizeof(sm2_init0_data)), ENCRYPT_MODE, FPGA_ECB_MODE, FPGA_DISABLE, 0, FPGA_DISABLE, 0);
	memcpy(data_ptr, sm2_init0_data, sizeof(sm2_init0_data));
	fpga_write_finish(fpga_header.pkglen);
	
//	print_byte((uint8_t *)FPGA_DATA_WRITE_ADDR, fpga_header.pkglen);
	
	//delay for safety
	delay_ms(1);
	
	//step: init 1
	memset(&fpga_header, 0, sizeof(FPGAHeader));
	while(fpga_write_start()==REG_REST) {
		fpga_reset_mcu();
		delay_ms(5);
	}
	fpga_header.mark = FPGA_HEAD_MARK;
	fpga_header.src = FPGA_DATA_ARM;
	fpga_header.dst = FPGA_DATA_SM2_HSM2;
	fpga_header.channel = FPGA_CHANNEL_DEF;
	fpga_header.sm2_cmd = CMD_SM2_INIT1;
	fpga_header.pkglen = FPGA_DATAHEAD_LEN + FPGA_ALGHEAD_LEN + FPGA_DATA_LEN(sizeof(sm2_init1_data));
	alg_hdr = set_fpga_header((uint8_t *)FPGA_DATA_WRITE_ADDR, &fpga_header);
	data_ptr = alg_header(alg_hdr, 0, FPGA_ALGHEAD_LEN + FPGA_DATA_LEN(sizeof(sm2_init1_data)), ENCRYPT_MODE, FPGA_ECB_MODE, FPGA_DISABLE, 0, FPGA_DISABLE, 0);
	memcpy(data_ptr, sm2_init1_data, sizeof(sm2_init1_data));
	fpga_write_finish(fpga_header.pkglen);
	
//	print_byte((uint8_t *)FPGA_DATA_WRITE_ADDR, fpga_header.pkglen);
	
	delay_ms(1);
	
	//step: init 2
	memset(&fpga_header, 0, sizeof(FPGAHeader));
	while(fpga_write_start()==REG_REST) {
		fpga_reset_mcu();
		delay_ms(5);
	}
	fpga_header.mark = FPGA_HEAD_MARK;
	fpga_header.src = FPGA_DATA_ARM;
	fpga_header.dst = FPGA_DATA_SM2_HSM2;
	fpga_header.channel = FPGA_CHANNEL_DEF;
	fpga_header.sm2_cmd = CMD_SM2_INIT2;
	fpga_header.pkglen = FPGA_DATAHEAD_LEN + FPGA_ALGHEAD_LEN + FPGA_DATA_LEN(sizeof(sm2_init2_data));
	alg_hdr = set_fpga_header((uint8_t *)FPGA_DATA_WRITE_ADDR, &fpga_header);
	data_ptr = alg_header(alg_hdr, 0, FPGA_ALGHEAD_LEN + FPGA_DATA_LEN(sizeof(sm2_init2_data)), ENCRYPT_MODE, FPGA_ECB_MODE, FPGA_DISABLE, 0, FPGA_DISABLE, 0);
	memcpy(data_ptr, sm2_init2_data, sizeof(sm2_init2_data));
	fpga_write_finish(fpga_header.pkglen);
	
	delay_ms(1);
	
//	print_byte((uint8_t *)FPGA_DATA_WRITE_ADDR, fpga_header.pkglen);

}

